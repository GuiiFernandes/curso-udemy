/* console.log é a função que vai mostrar na tela aquilo que queremos ver. Para debugar. 
Console não mostra nada na tela ou no site, somente no console do Inspecionar ou no console do VSCode.*/
//Para expressar textos utilizamos ", ' ou `
console.log('Guilherme "Fernandes"'); /* A diferença entre ", ' e ` é que se eu precisar usar um sinal dentro de outro igual o js não aceita.*/
console.log("Guilherme 'Fernandes'");
console.log(`'Guilherme' "Fernandes"`); /*Crase também é utilizado para template strings. Mas ele também aceita ambos os sinais.*/
/*Texto é um tipo de dado chamado string. */

// Posso exibir número também.
console.log(123456, 154543.85435353); //números são usados de forma literal e números decimais são separados por . e não ,.
// Quando utilizo , e coloco vários valores no console.log ele não quebra a linha.
console.log(35, 15.85, "Gui Fernandes"); // Número é um tipo de dado chamado number. No Js não separamos os dados decimais com inteiros.